number = int(input("Введите число: "))

if number < 0:  # Если число отрицательное
    number = -number  # Преобразуем его в положительное

while number >= 10:  # Пока число содержит больше одной цифры
    number //= 10  # Убираем последнюю цифру

print("Первая цифра числа:", number)

# task_3
number = int(input("Введите натуральное число: "))
min_digit = 9  # Максимально возможная цифра для начала

while number > 0:
    digit = number % 10  # Берем последнюю цифру
    if digit < min_digit:
        min_digit = digit  # Обновляем минимальную цифру
    number //= 10  # Убираем последнюю цифру

print("Минимальная цифра в числе:", min_digit)


