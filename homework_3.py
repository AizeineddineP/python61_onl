# Ввод двух списков
list1 = list(map(int, input("Введите элементы первого списка через пробел: ").split()))
list2 = list(map(int, input("Введите элементы второго списка через пробел: ").split()))

# Находим элементы из первого списка, которых нет во втором
unique_elements = [x for x in list1 if x not in list2]

# Если такие элементы есть, находим наименьший
if unique_elements:
    print("Наименьший элемент, который есть в первом списке, но нет во втором:", min(unique_elements))
else:
    print("Нет элементов в первом списке, которые не входят во второй.")

